#include "libft.h"

char *ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t p;

	i = 0;
	p = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[p] != '\0')
	{
		if (p >= n)
		{
			s1[i] = '\0';
			break ;
		}
		s1[i] = s2[p];
		i++;
		p++;
	}
	s1[i] = '\0';
	return (s1);
}
